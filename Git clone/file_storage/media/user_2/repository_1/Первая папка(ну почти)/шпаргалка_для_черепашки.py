
import turtle
z = turtle.Pen()  # ручка(создание)
z.color(1,1,0)    # цвет обьекта
z.begin_fill()    # начать закрашивание
z.forward(100)    # линия
z.left(90)        # поворот влево
z.end_fill()      # закончить закрашивание

z.up()            # поднять перо
z.circle(100)     # круг
z.right(90)       # поворот направо
z.down()          # опустить перо
z.speed(0)        # скорость



while 1 == 1 :
    z.forward(100)
    z.left(175)
    z.forward(80)
    z.left(69)
    z.circle(100)
    

for i in range(1,20):
    z.forward(100)
    z.left(175)    
    
